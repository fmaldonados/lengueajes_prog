    void refrescar_gramatica() {
        File archivo = new File("./SimpleGrammarES2.txt");
        FileWriter fw = null;
        BufferedWriter bw = null;
        ArrayList<String> nombres = new ArrayList();
        try {
            fw = new FileWriter(archivo);
            bw = new BufferedWriter(fw);
            String texto = "#JSGF V1.0;\n"
                    + "grammar sentence;\n"
                    + " \n"
                    + "public <sentence> =\n"
                    + "[<dato0>]\n"
                    + "[<dato1>]\n"
                    + "[<dato2>]\n"
                    + "[<dato3>]\n"
                    + "[<dato4>]\n"
                    + "[<dato5>]\n"
                    + "[<dato6>]\n"
                    + "[<dato7>]\n"
                    + "[<dato8>]\n"
                    + "[<dato9>]\n"
                    + "[<dato10>]\n"
                    + "[<dato11>]\n"
                    + "[<dato12>]\n"
                    + "[<dato13>]\n"
                    + "[<dato14>]\n"
                    + "[<dato15>]";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from dbo.Contacto");
            int length = 15;
            while (rs.next()) {
                length++;
                nombres.add(rs.getString(2));
                texto += "\n[<dato" + length + ">]";
            }
            texto += "\n[<dato1><dato2><dato3><dato4><dato5><dato6><dato7><dato8><dato9><dato10><dato11><dato12><dato13><dato14><dato15>";
            for (int i = 16; i <= length; i++) {
                texto += "<dato" + i + ">";
            }
            texto += "];";
            texto += "<dato0>=Agregar;\n"
                    + "<dato1>=Modificar;\n"
                    + "<dato2>=User;\n"
                    + "<dato3>=Delete;\n"
                    + "<dato4>=Password;\n"
                    + "<dato5>=Call;\n"
                    + "<dato6>=Mensaje;\n"
                    + "<dato7>=Activar;\n"
                    + "<dato8>=Desactivar;\n"
                    + "<dato9>=Camara;\n"
                    + "<dato10>=Salir;\n"
                    + "<dato11>=Brian;\n"
                    + "<dato12>=Disco;\n"
                    + "<dato13>=Contacto;\n"
                    + "<dato14>=Search;\n"
                    + "<dato15>=All;";
            for (int i = 1; i <= nombres.size(); i++) {
                texto += "<dato" + (i + 15) + ">=" + nombres.get(i - 1) + ";";
            }
            bw.write(texto);
            bw.flush();
        } catch (Exception e) {
            e.printStackTrace();
        }
        try {
            bw.close();
            fw.close();
        } catch (Exception e) {
        }

    }


